class TerminalColors():
    # Color Reset
    Color_Off="\033[0m"       # Text Reset

    # Regular Colors
    Black="\033[0;30m"        # Black
    Red="\033[0;31m"          # Red
    Green="\033[0;32m"        # Green
    Yellow="\033[0;33m"       # Yellow
    Blue="\033[0;34m"         # Blue
    Purple="\033[0;35m"       # Purple
    Cyan="\033[0;36m"         # Cyan
    White="\033[0;37m"        # White

    # Bold
    lackBold="\033[1;30m"       # Black
    RedBold="\033[1;31m"         # Red
    GreenBold="\033[1;32m"       # Green
    YellowBold="\033[1;33m"      # Yellow
    BlueBold="\033[1;34m"        # Blue
    PurpleBold="\033[1;35m"      # Purple
    CyanBold="\033[1;36m"        # Cyan
    WhiteBold="\033[1;37m"       # White

    # Underline
    BlackUnderline="\033[4;30m"       # Black
    RedUnderline="\033[4;31m"         # Red
    GreenUnderline="\033[4;32m"       # Green
    YellowUnderline="\033[4;33m"      # Yellow
    BlueUnderline="\033[4;34m"        # Blue
    PurpleUnderline="\033[4;35m"      # Purple
    CyanUnderline="\033[4;36m"        # Cyan
    WhiteUnderline="\033[4;37m"       # White

    # Background
    BlackBackground="\033[40m"       # Black
    RedBackground="\033[41m"         # Red
    GreenBackground="\033[42m"       # Green
    YellowBackground="\033[43m"      # Yellow
    BlueBackground="\033[44m"        # Blue
    PurpleBackground="\033[45m"      # Purple
    CyanBackground="\033[46m"        # Cyan
    WhiteBackground="\033[47m"       # White

    # High Intensty
    BlackIntense="\033[0;90m"       # Black
    RedIntense="\033[0;91m"         # Red
    GreenIntense="\033[0;92m"       # Green
    YellowIntense="\033[0;93m"      # Yellow
    BlueIntense="\033[0;94m"        # Blue
    PurpleIntense="\033[0;95m"      # Purple
    CyanIntense="\033[0;96m"        # Cyan
    WhiteIntense="\033[0;97m"       # White

    # Bold High Intensty
    BlackBoldIntense ="\033[1;90m"      # Black
    RedBoldIntense="\033[1;91m"        # Red
    GreenBoldIntense="\033[1;92m"      # Green
    YellowBoldIntense="\033[1;93m"     # Yellow
    BlueBoldIntense="\033[1;94m"       # Blue
    PurpleBoldIntense="\033[1;95m"     # Purple
    CyanBoldIntense="\033[1;96m"       # Cyan
    WhiteBoldIntense="\033[1;97m"      # White

    # High Intensty backgrounds
    BlackIntenseBackground="\033[0;100m"   # Black
    RedIntenseBackground="\033[0;101m"     # Red
    GreenIntenseBackground="\033[0;102m"   # Green
    YellowIntenseBackground="\033[0;103m"  # Yellow
    BlueIntenseBackground="\033[0;104m"    # Blue
    PurpleIntenseBackground="\033[10;95m"  # Purple
    CyanIntenseBackground="\033[0;106m"    # Cyan
    WhiteIntenseBackground="\033[0;107m"   # White

